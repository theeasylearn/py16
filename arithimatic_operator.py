#example of arthimatic operator
num1 = input("enter first number")
num2 = input("enter second number")

#convert value into integer
num1 = int(num1)
num2 = int(num2)

addition = num1 + num2
substraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2
reminder = num1 % num2
floor_division = num1 // num2 
power = num1 ** num2  

print("addition ",addition," multiplication ",multiplication," division ",division," reminder ",reminder," floor division ",floor_division," power ",power)

print(f"addition = {addition} substraction = {substraction} multiplication = {multiplication}")