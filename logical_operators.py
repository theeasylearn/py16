#example of relational operators
num1 = int(input('enter first number')) #10
num2 = int(input('enter second number')) # 20
num3 = int(input('enter third number')) # 20
result = num1 == num2 and num2 == num3 
print("num1 == num2 and num2 == num3  ",result)

result = num1 == num2 or num2 == num3 
print("num1 == num2 or num2 == num3  ",result)

