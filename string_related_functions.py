name = "the easylearn academy"
print(name)
print(f"uppercase {name} ",name.upper())
print(f"lowercase {name} ",name.lower())
print(f"length of {name} ",len(name))

print(f"is {name} is alpha numeric",name.isalnum())
print(f"is {name} is alphabatic ",name.isalpha())
print(f"is {name} is upper alphabatic ",name.isupper())
print(f"is {name} is lower alphabatic ",name.islower())
print(f"is {name} has only numberis ",name.isnumeric())
